def sumar(a,b):
    return print("Suma: ", a+b)
def restar(a,b):
    return print("Resta: ", a-b)
def multiplicar(a,b):
    return print("Multiplicacion: ", a*b)
def dividir(a,b):
    if b==0:
        return print("No se puede dividir por 0")
    else:
        return a/b
